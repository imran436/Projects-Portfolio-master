def Subtraction(A,B):
    subtract = A-B
    return subtract
print(Subtraction(20,10))
print(Subtraction(10,5))
print(Subtraction(500,350))
def addition(C,D,E):
    add = C+D+E
    return add
print(addition(5,10,20))
def multiplication(F,G,H):
    multiply = F*G*H
    return multiply
print(multiplication(2,4,6))
def division(I,J,K):
    divide = I/J//K
    return divide
print(division(20,4,2))
